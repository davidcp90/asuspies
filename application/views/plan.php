     <h4>El hombre es pecador</h4>
<p>"Por cuanto todos pecaron, y están destituidos de la gloria de Dios." <b><i>Romanos 3:23</i></b></p>
<p>El hombre fue creado para tener relación perfecta con Dios, pero debido a su egocentrismo y desobediencia, 
escogió su propio camino y la relación con Dios se interrumpió. Este acto de voluntad propia que se manifiesta 
por una actitud de rebelión activa o indiferencia pasiva, es una evidencia de lo que la Biblia llama pecado.</p> 
